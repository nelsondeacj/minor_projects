import os 
   
# Get the value of 'HOME' 
key1 = 'HOME'
value = os.getenv(key1) 
   
# Print the value of 'HOME' 
# environment variable 
print("Value of 'HOME' environment variable :", value)  

# Get the value of 'Path' 
key2 = 'PATH'
value = os.getenv(key2) 

# Print the value of 'Path' 
# environment variable 
print("Value of 'Path' environment variable :", value)  
   
